def take_intersection(left_nodeset, right_nodeset):
    """
    Returns the intersection of two nodesets.
    Corresponds to the `Intersection` operator in the instructions.

    Args:
        left_nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        right_nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.

    Returns:
        list: A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        It contains nodes present in both nodesets.
    """

    left_ids = {node['NodeKey'] for node in left_nodeset}
    right_ids = {node['NodeKey'] for node in right_nodeset}

    common_ids = left_ids & right_ids

    common_nodeset = [node for node in left_nodeset + right_nodeset if node['NodeKey'] in common_ids]
    common_nodeset = drop_duplicates(common_nodeset)

    return common_nodeset


def take_union(left_nodeset, right_nodeset):
    """
    Returns the union of two nodesets.
    Corresponds to the `Union` operator in the instructions.

    Args:
        left_nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        right_nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.

    Returns:
        list: A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        It contains nodes present in either of the nodesets.
    """

    union_nodeset = drop_duplicates(left_nodeset + right_nodeset)

    return union_nodeset


def take_difference(left_nodeset, right_nodeset):
    """
    Returns the set difference of two nodesets.
    Corresponds to the `Difference` operator in the instructions.

    Args:
        left_nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        right_nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.

    Returns:
        list: A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        It contains nodes present in the first nodeset which are not in the second nodeset.
    """

    right_ids = {node['NodeKey'] for node in right_nodeset}

    difference_nodeset = [node for node in left_nodeset if node['NodeKey'] not in right_ids]

    return difference_nodeset
