def filter(nodeset, key, value):
    """
    Filters a nodeset according to a field's value or range.
    The key identifier is used to infer which field of the nodes database table is used.
    If no field can be inferred, the value is matched against the Content field on elasticsearch.
    Corresponds to the `Filter` and `FilterRange` operators in the instructions.

    Args:
        nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        key (str): Either the database field name or some recognised synonym (e.g. `Gender`).
        value (str|int|float|tuple): Either the database value (or interval) for the `key` field or some recognised synonym (e.g. `Female`).

    Returns:
        list: A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`. It is always a subset of `nodeset`.
    """

    if len(nodeset) == 0:
        return []

    # Assuming all nodes in nodeset are of the same type
    node_type = nodeset[0]['NodeType']

    # Extract ids from nodeset
    ids = [node['NodeKey'] for node in nodeset]

    try:
        filtered_ids = filter_node_ids(node_type, key, value, filter_ids=ids)
    except Exception as e:
        # If the above does not work as expected, just search both key and value on the Content field in elasticsearch
        nodeset = es.search_node_contents(value, node_type, filter_ids=ids)
        filtered_ids = [node['NodeKey'] for node in nodeset]

    # Filter nodeset, keep only ids found above
    filtered_nodeset = [node for node in nodeset if node['NodeKey'] in filtered_ids]

    return filtered_nodeset


def sort(nodeset, key, order):
    """
    Sorts a nodeset according to a field's value.
    Corresponds to the `Sort` operator in the instructions.

    Args:
        nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        key (str): Either the database field name or some recognised synonym (e.g. `Gender`).
        order (str): Either `ASC` or `DESC` or some recognised synonym.

    Returns:
        list: A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        It contains the same nodes as nodeset but rearranged.
    """

    if len(nodeset) == 0:
        return []

    # Assuming all nodes in nodeset are of the same type
    node_type = nodeset[0]['NodeType']

    # Build table name
    table_name, id_field = get_node_db_info(node_type)

    # Extract ids from nodeset
    ids = [node['NodeKey'] for node in nodeset]

    # Project implemented (node_type, key) to field name
    key_field = get_key_field(node_type, key)

    # Project order to database syntax
    if 'DESC' in order or 'Desc' in order or 'desc' in order:
        key_order = 'DESC'
    else:
        key_order = 'ASC'

    # Sort the node ids table using the (key_field, key_value)
    query = f"""
        SELECT {id_field}
        FROM graphsearch.{table_name}
        WHERE {id_field} IN ({', '.join(['%s'] * len(ids))})
        ORDER BY {key_field} {key_order}
    """

    results = db_manager.db.execute_query(query, ids)
    sorted_ids = [str(r) for r, in results]

    # Filter nodeset, keep only ids found above
    filtered_nodeset = [node for node in nodeset if node['NodeKey'] in sorted_ids]

    # Sort nodeset according to returned order
    sorted_nodeset = sorted(filtered_nodeset, key=lambda node: sorted_ids.index(node['NodeKey']))

    return sorted_nodeset


def limit(nodeset, n):
    """
    Returns the first `n` nodes of a nodeset.
    Corresponds to the `Limit` operator in the instructions.

    Args:
        nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        n (int): The maximum number of nodes to return.

    Returns:
        list: A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        If nodeset has less than `n` nodes, it returns `nodeset`.
    """

    return nodeset[:n]


