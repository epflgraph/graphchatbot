def search_node(node_type, text):
    """
    Retrieves (from elasticsearch) the node of the given type that better matches the given text.
    Corresponds to the `Search` operator in the instructions.

    Args:
        node_type (str): The type of the node (e.g. `Concept`, `Person`).
        text (str): The piece of text to search for in the node text fields.

    Returns:
        dict: A dictionary with the node `NodeKey`, `NodeType` and `Title`.
    """

    nodeset = es.search_nodes(text, node_type)
    return nodeset[:1]


def get_all_nodes_and_filter(node_type, key, value):
    """
    Retrieves all nodes of the given type filtering by key and value.
    Corresponds to the `All` operator in the instructions.

    Args:
        node_type (str): The type of the node (e.g. `Concept`, `Person`).
        key (str): Either the database field name or some recognised synonym (e.g. `Gender`).
        value (str): Either the database value for the `key` field or some recognised synonym (e.g. `Female`).

    Returns:
        list: A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
    """

    # Get node ids
    node_ids = filter_node_ids(node_type, key, value)

    # Get nodeset from ids
    nodeset = es.get_nodeset(node_ids, node_type)

    return nodeset


def get_neighborhood(nodeset, node_type, return_order=False):
    """
    Retrieves the neighbours of a nodeset of the given type.
    Corresponds to the `Neighborhood` operator in the instructions.

    Args:
        nodeset (list): A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        node_type (str): The type of the node (e.g. `Concept`, `Person`).
        return_order (bool): Whether to return the order of the node in the list of neighbours.

    Returns:
        list: A list of dictionaries, each representing a node, with the keys `NodeKey`, `NodeType` and `Title`.
        If `return_order` is True, the key `Order` is set too.
    """

    if len(nodeset) == 0:
        return []

    # Assuming all nodes in nodeset are of the same type
    source_node_type = nodeset[0]['NodeType']
    target_node_type = node_type

    # Build table name
    db_info = get_edge_db_info(source_node_type, target_node_type)
    table_name, source_id_field, target_id_field, order_field = db_info

    # Extract ids from nodeset
    ids = [node['NodeKey'] for node in nodeset]

    # Build and run query
    query = f"""
        SELECT DISTINCT {target_id_field}
        FROM graphsearch.{table_name}
        WHERE {source_id_field} IN ({', '.join(['%s'] * len(ids))})
    """

    if order_field is not None:
        query += f"""ORDER BY {order_field} DESC"""

    results = db_manager.db.execute_query(query, ids)
    neighbor_ids = [r for r, in results]

    # Remove duplicates while keeping order
    neighbor_ids = list(dict.fromkeys(neighbor_ids))

    # Get nodes from ids
    nodes = es.get_nodeset(neighbor_ids, target_node_type)
    nodes = drop_duplicates(nodes)

    # Return order field if required
    if return_order:
        query = f"""
            SELECT {target_id_field}, MAX({order_field})
            FROM graphsearch.{table_name}
            WHERE {source_id_field} IN ({', '.join(['%s'] * len(ids))})
            AND {target_id_field} IN ({', '.join(['%s'] * len(neighbor_ids))})
            GROUP BY {target_id_field}
        """

        results = db_manager.db.execute_query(query, ids + neighbor_ids)

        for node in nodes:
            node['Order'] = 0
            for node_id, order_value in results:
                if node['NodeKey'] == node_id:
                    node['Order'] = order_value

    return nodes


